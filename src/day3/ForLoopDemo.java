package day3;

public class ForLoopDemo {

	public static void main(String[] args) {

		// For ( start point; logic; increment/decrement ) { code block }

		for (int i = 1; i <= 5; i++) {
			System.out.println(i);

		}
		System.out.println();

		for (int i = 1; i <= 5; i++) {
			System.out.println("x");

		}
		System.out.println();

		for (int i = 10; i >= 1; i--) {
			System.out.println(i);

		}

	}

}
